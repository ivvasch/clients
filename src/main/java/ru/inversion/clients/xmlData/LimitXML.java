package ru.inversion.clients.xmlData;

import java.time.LocalDate;

/**
 * Класс связан с основным классом Limits, который включает в себя все лимиты по картам. Который относится к одному карточному счету.
 */
public class LimitXML {
    private LocalDate date;
    private String val;

    public LimitXML() {
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }
}
