package ru.inversion.clients.xmlData;

import java.util.ArrayList;
import java.util.List;

public class LimitsXML {
    private List<LimitXML> listLimts;

    public LimitsXML() {
    }

    public List<LimitXML> getListLimts() {
        if (listLimts == null) {
            listLimts = new ArrayList<>();
            return listLimts;
        }
        return listLimts;
    }

    public void setListLimts(List<LimitXML> listLimts) {
        this.listLimts = listLimts;
    }
}
